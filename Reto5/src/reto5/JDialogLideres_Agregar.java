    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (validateJavaDate(txtFechaNa.getText())) {
            LideresVo l = new LideresVo();
            l.setNombre(txtNombre.getText());
            l.setPrimerApellido(txtPApellido.getText());
            l.setSegundoApellido(txtSApellido.getText());
            l.setSalario(Integer.parseInt(txtSalario.getText()));
            l.setCiudadResidencia(txtCiudad.getText());
            l.setCargo(jcomboCargo.getSelectedItem().toString());
            l.setClasificacion(Integer.parseInt(jcomboClasificacion.getSelectedItem().toString()));
            l.setDocumentoIdentidad(txtNumDocumento.getText());
            l.setFechaNacimiento(txtFechaNa.getText());

            try {
                controlador.insertarLider(l);
                JOptionPane.showMessageDialog(this, "Se ha insertado correctamente ");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, " " + e.getMessage());
                System.out.println(e.getMessage());
            }

            System.out.println("ok");
        } else {
            JOptionPane.showMessageDialog(this, txtFechaNa.getText() + " Esta fecha esta incorrecta");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

