    boolean isWithinRange(Date testDate, Date startDate, Date endDate) {
        return testDate.after(startDate) && testDate.before(endDate);
    }

    private boolean validateJavaDate(String strDate) {
        /* Check if date is 'null' */
        if (strDate.trim().equals("")) {
            return true;
        } /* Date is not 'null' */ else {
            /*
	     * Set preferred date format,
	     * For example MM-dd-yyyy, MM.dd.yyyy,dd.MM.yyyy etc.*/
            SimpleDateFormat sdfrmt = new SimpleDateFormat("yyyy-MM-dd");
            sdfrmt.setLenient(false);
            /* Create Date object
	     * parse the string into date 
             */
            try {
                Date javaDate = sdfrmt.parse(strDate);
                Date startDate = sdfrmt.parse("1900-01-01");
                Date endDate = sdfrmt.parse("2021-12-31");

                if (isWithinRange(javaDate, startDate, endDate)) {

                    System.out.println(strDate + " is valid date format");
                } else {
                    JOptionPane.showMessageDialog(this, txtFechaNa.getText() + " Esta fecha esta incorrecta");
                    System.out.println(strDate + " is Invalid Date format");
                }
            } /* Date format is invalid */ catch (ParseException e) {
                System.out.println(strDate + " is Invalid Date format");
                return false;
            }
            /* Return true if date format is valid */
            return true;
        }
    }

